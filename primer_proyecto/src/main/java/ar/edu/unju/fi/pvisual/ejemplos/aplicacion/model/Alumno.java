package ar.edu.unju.fi.pvisual.ejemplos.aplicacion.model;

public class Alumno {
	private String nombre;
	private String apellido;
	private int edad;
	
	public Alumno () {
		
	}

	public Alumno(String nombre, String apellido, int edad) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.edad = edad;
	}
	
	
}
